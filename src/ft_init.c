#include "test.h"

void	ft_init(int size)
{
	int		x;
	int		y;

	x = 0;
	y = 0;
	while (y < size)
	{
		while (x < size)
		{
			yf[y].xf[x] = '.';
			x++;
		}
		yf[y].xf[x] = '\n';
		x = 0;
		y++;
	}
	yf[size].xf[0] = '\0';
}
